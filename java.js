var age = +prompt('Yoshingiz nechchida')

if (age >= 60) {
	alert('Siz nafaqa yoshida ekansiz')
} else if (age >= 50) {
	alert('Siz yaqinda nafaqa yoshiga yetarkansiz')
} else if (age >= 49) {
	alert('Siz hali ishlashingiz kerak')
} else if (age >= 19) {
	alert('Siz ishlashingiz kerak')
} else if (age <= 18) {
	alert('Siz hali yoshsiz oqisingiz kerak')
}
